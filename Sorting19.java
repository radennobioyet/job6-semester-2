public class Sorting19 {

    int[] data;
    int jmlData;

    Sorting19(int[] data, int jmlData) {
        this.data = data;
        this.jmlData = jmlData;
    }

    void bubbleSort() {
        for (int i = 0; i < jmlData - 1; i++) {
            for (int j = 0; j < jmlData - i - 1; j++) {
                if (data[j] > data[j + 1]) {
                    int tmp = data[j];
                    data[j] = data[j + 1];
                    data[j + 1] = tmp;
                }
            }
        }
    }

    void tampil() {
        for (int i = 0; i < jmlData; i++) {
            System.out.print(data[i] + " ");
        }
        System.out.println();
    }
}
